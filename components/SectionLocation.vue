<script>

import AppIcon from '@/components/AppIcon.vue'
import MapLink from '@/components/MapLink'
import ParallaxContainer from '@/components/ParallaxContainer'
import ParallaxElement from '@/components/ParallaxElement'
import scrollObserver from '@/mixins/scrollObserver'

export default {
    name: 'section-location',
    components: {
        AppIcon,
        ParallaxContainer,
        ParallaxElement,
        MapLink
    },
    computed: {
        location() {
            return this.$store.getters.location
        }
    },
    mixins: [scrollObserver],
    methods: {
        animateSection() {
            const {$refs} = this
            const { 
                map, 
                mapPin, 
                streetNames, 
                fwySigns,
                hours, 
                hoursTitle, 
                hoursList
            } = $refs

            const title = new SplitText(this.$refs.title)
            const address1 = new SplitText(this.$refs.address1).chars
            const address2 = new SplitText(this.$refs.address2).chars
            const address = [...address1, ...address2]

            return new TimelineMax({ paused: true })

            //title
            .add('title', 0.5)
            .staggerFromTo(title.chars, 2, {
                x: 15,
                autoAlpha: 0
            }, {
                x: 0,
                autoAlpha: 1,
                ease: Power3.easeOut
            }, 0.025, 'title')
            .staggerFromTo(address, 2, {
                x: 15,
                autoAlpha: 0
            }, {
                x: 0,
                autoAlpha: 1,
                ease: Power3.easeOut
            }, 0.025, 'title')

            //map
            .add('map', 0)
            .staggerFromTo(map.children, 10, {
                drawSVG: '50% 50%',
                ease: Expo.easeIn
            }, {
                drawSVG: '100%',
                ease: Expo.easeOut
            }, 0.1, 'map')
            .staggerFromTo(streetNames.children, 1, {
                autoAlpha: 0,
                y: -15,
            }, {
                autoAlpha: 1,
                y: 0,
                ease: Sine.easeOut
            }, 0.3, 'map+=1')
            .staggerFromTo(fwySigns.children, 1, {
                autoAlpha: 0,
                scale: 1.5
            }, {
                autoAlpha: 1,
                scale: 1,
                transformOrigin: '50% 50%',
                ease: Sine.easeOut
            }, 0.3, 'map+=1.2')
            .fromTo(mapPin, 0.3, {
                opacity: 0,
            }, {
                opacity: 1,
                ease: Sine.easeOut
            }, 'map')
            .fromTo(mapPin, 2.5, {
                y: -window.innerHeight * 0.25,
            }, {
                x: 287, 
                y: 113,
                ease: Bounce.easeOut
            }, 'map')

            // Hours
            .add('hours', 1.25)
            .fromTo(hours.$el, 1, {
                clipPath: '100% 0 0 0',
                webkitClipPath: '100% 0 0 0'
            }, {
                clipPath: '0 0 0 0',
                webkitClipPath: '0 0 0 0',
                ease: Power3.easeOut
            }, 'hours')
            .fromTo(hoursTitle, 1, {
                y: -15,
                autoAlpha: 0
            }, {
                y: 0,
                autoAlpha: 1,
                ease: Power3.easeOut
            }, 'hours+=0.5')
            .staggerFromTo(hoursList.children, 1, {
                y: -15,
                autoAlpha: 0
            }, {
                y: 0,
                autoAlpha: 1,
                ease: Power3.easeOut
            }, 0.05, 'hours+=0.6')
        }
    }
}
</script>

