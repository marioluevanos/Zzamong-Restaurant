                    x: '0vw',
                    rotation: `${getRandomInt(-5, 5)}deg`,
                    ease: Expo.easeInOut,
                    delay: idx * 0.05,
                    zIndex
                })

                if (idx === polaroids.length - 1) {
                    this.currentIndex = 0
                }
            })
        },
        staggerPics() {
            setTimeout(() => {
                this.polaroids.forEach(p => {
                    p.style.transform = `translate3d(0, ${getRandomInt(-2, 2)}px, 0) rotate(${getRandomInt(-4, 4)}deg)`
                })
            }, 1000)
